
public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World"); // String 캐릭터들이 모여있는 문자열을 나타낸다 ! 
    //  System.out.println('Hello World'); // Character 문자를 나타낸다 한글자를 나타내는 데이터타입
		System.out.println('H');
		System.out.println("Hello "
				+ "World");
		// new line
		System.out.println("Hello \nWorld");  // 띄어쓰기 
		
		// escape 
		System.out.println("Hello \"World\"");// Hello "World" 만약 진짜 " 를 표현하고 싶을때 escape 사용
				
	}

}
